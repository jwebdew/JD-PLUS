    insertAppScript: function() {
        if (typeof EC_APPSCRIPT_ASSIGN_DATA !== "undefined" && Array.isArray(window.EC_APPSCRIPT_ASSIGN_DATA)) {
            while (EC_APPSCRIPT_ASSIGN_DATA.length > 0) {
                var oSrcData = EC_APPSCRIPT_ASSIGN_DATA.pop();
